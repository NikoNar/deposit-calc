// ── Core calculation ──────────────────────────────────────────────────────────
function calcDeposit({ principal, rate, monthlyAdd, addMonths, years, taxRate, paymentsPerYear = 1 }) {
  const monthsPerPeriod = 12 / paymentsPerYear;
  const periodRate = rate / 100 / paymentsPerYear;
  let allRows = [], balance = principal, totalContributed = principal;
  let totalInterestGross = 0, totalTax = 0, periodStartBalance = principal;

  for (let year = 0; year < years; year++) {
    for (let m = 0; m < 12; m++) {
      const monthInYear = m + 1;
      const isPaymentMonth = (monthInYear % monthsPerPeriod === 0);
      let contribution = 0;
      if (monthInYear <= addMonths && monthlyAdd > 0) {
        contribution = monthlyAdd;
        balance += contribution;
        totalContributed += contribution;
      }
      let grossInterest = 0, tax = 0, netInterest = 0;
      if (isPaymentMonth) {
        grossInterest = periodStartBalance * periodRate;
        tax = grossInterest * (taxRate / 100);
        netInterest = grossInterest - tax;
        totalInterestGross += grossInterest;
        totalTax += tax;
        balance += netInterest;
        periodStartBalance = balance;
      }
      allRows.push({ month: year * 12 + m + 1, year: year + 1, monthInYear, balance, grossInterest, netInterest, tax, contribution, totalContributed, totalInterestGross, totalTax, isPaymentMonth });
    }
  }
  const finalBalance = balance;
  const netProfit = finalBalance - totalContributed;
  const yearlyRows = [];
  for (let y = 1; y <= years; y++) {
    const endRow = allRows[y * 12 - 1];
    const yr = allRows.filter(r => r.year === y);
    yearlyRows.push({
      year: y, balance: endRow.balance,
      yearInterestGross: yr.reduce((s, r) => s + r.grossInterest, 0),
      yearInterestNet:   yr.reduce((s, r) => s + r.netInterest, 0),
      yearContributions: yr.reduce((s, r) => s + r.contribution, 0),
      totalContributed:  endRow.totalContributed,
    });
  }
  return { allRows, yearlyRows, finalBalance, totalContributed, totalInterestGross, totalTax, netProfit };
}

function calcEarlyWithdrawal({ principal, rate, monthlyAdd, addMonths, years, taxRate, withdrawMonth, penaltyRate, paymentsPerYear }) {
  const full = calcDeposit({ principal, rate, monthlyAdd, addMonths, years, taxRate, paymentsPerYear });
  const rowAt = full.allRows[withdrawMonth - 1];
  if (!rowAt) return null;
  const penaltyRows = calcDeposit({ principal, rate: penaltyRate, monthlyAdd, addMonths, years, taxRate, paymentsPerYear }).allRows;
  const penaltyBalance = penaltyRows[withdrawMonth - 1]?.balance ?? principal;
  return { balanceAtMonth: rowAt.balance, contributed: rowAt.totalContributed, penaltyBalance, loss: Math.max(0, rowAt.balance - penaltyBalance) };
}

function calcGoal({ target, rate, monthlyAdd, addMonths, taxRate, paymentsPerYear, maxYears = 100 }) {
  for (let y = 1; y <= maxYears; y++) {
    const res = calcDeposit({ principal: 0, rate, monthlyAdd, addMonths, years: y, taxRate, paymentsPerYear });
    if (res.finalBalance >= target) return { years: y, result: res };
  }
  return null;
}
function calcGoalPrincipal({ target, rate, monthlyAdd, addMonths, years, taxRate, paymentsPerYear }) {
  let lo = 0, hi = target * 3;
  for (let i = 0; i < 60; i++) {
    const mid = (lo + hi) / 2;
    const res = calcDeposit({ principal: mid, rate, monthlyAdd, addMonths, years, taxRate, paymentsPerYear });
    res.finalBalance < target ? (lo = mid) : (hi = mid);
  }
  return Math.ceil(hi);
}

