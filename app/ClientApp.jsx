function NumIn({ value, onChange, min, max, step, T }) {
  const [displayValue, setDisplayValue] = useState(String(value));
  const lastValueRef = useRef(value);

  // Sync from parent (e.g. slider or external change) – don't overwrite while user has cleared the field
  useEffect(() => {
    if (value !== lastValueRef.current) {
      lastValueRef.current = value;
      setDisplayValue(String(value));
    }
  }, [value]);

  const parseNumber = (raw) => {
    if (raw === "" || raw === "-") return null;
    // Allow both . and , as decimal separator (locale-friendly)
    const normalizedRaw = raw.replace(",", ".");
    const num = (step >= 1 && Number.isInteger(step)) ? parseInt(normalizedRaw, 10) : parseFloat(normalizedRaw);
    return Number.isNaN(num) ? null : num;
  };

  const clamp = (n) => {
    if (n == null) return null;
    let c = n;
    if (min != null && c < min) c = min;
    if (max != null && c > max) c = max;
    return c;
  };

  const handleChange = (e) => {
    const raw = e.target.value;
    setDisplayValue(raw);
    if (raw === "" || raw === "-") {
      return; // allow empty; don't set 0
    }
    const num = parseNumber(raw);
    if (num == null) return;
    const clamped = clamp(num);
    if (clamped != null) {
      lastValueRef.current = clamped;
      onChange(clamped);
      // Normalize display, but not while user is typing a decimal (e.g. "9." → "9.1" or "4,5" in locale)
      const normalized = (step >= 1 && Number.isInteger(step))
        ? String(Math.round(clamped))
        : String(clamped);
      const isTypingDecimal = raw.endsWith(".") || raw.endsWith(",");
      if (!isTypingDecimal) {
        setDisplayValue(normalized);
      }
    }
  };

  const handleBlur = () => {
    const num = parseNumber(displayValue);
    if (displayValue === "" || displayValue === "-" || num == null) {
      const fallback = min != null ? min : 0;
      setDisplayValue(String(fallback));
      lastValueRef.current = fallback;
      onChange(fallback);
    } else {
      const clamped = clamp(num);
      if (clamped != null) {
        const normalized = (step >= 1 && Number.isInteger(step))
          ? String(Math.round(clamped))
          : String(clamped);
        setDisplayValue(normalized);
        lastValueRef.current = clamped;
        onChange(clamped);
      }
    }
  };

  return (
    <input
      type="text"
      inputMode="decimal"
      value={displayValue}
      min={min}
      max={max}
      step={step}
      onChange={handleChange}
      onBlur={handleBlur}
      style={{ width: "100%", background: T.surfaceAlt, border: `1px solid ${T.border}`, borderRadius: 8, color: T.text, padding: "8px 12px", fontSize: 14, fontFamily: "inherit", outline: "none", transition: "border-color .15s" }}
    />
  );
}
