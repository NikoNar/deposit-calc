async function downloadExcel(type, result, rate, years, principal, monthlyAdd, addMonths, currency, paymentsPerYear, lang = "en") {
  const wb = XLSX.utils.book_new();
  const sym = CURRENCY_SYMBOLS[currency];
  const r = n => Math.round(n);
  const L = EXCEL_STRINGS[lang] || EXCEL_STRINGS.en;
  const freqLabel = (lang === "hy" ? { 1: "Տարեկան", 2: "Կիսամյակ", 4: "Եռամսյակ", 12: "Ամսական" } : { 1: "Annually", 2: "Semi-Annual", 4: "Quarterly", 12: "Monthly" })[paymentsPerYear] ?? "";
  if (type === "yearly") {
    const hdrs = [L.year, `${L.opening} (${sym})`, `${L.topUps} (${sym})`, `${L.grossInterest} (${sym})`, `${L.tax10} (${sym})`, `${L.netInterest} (${sym})`, `${L.closing} (${sym})`];
    let ob = principal;
    const rows = result.yearlyRows.map(row => {
      const out = [`${L.year} ${row.year}`, r(ob), r(row.yearContributions), r(row.yearInterestGross), r(row.yearInterestGross - row.yearInterestNet), r(row.yearInterestNet), r(row.balance)];
      ob = row.balance; return out;
    });
    rows.push([L.total, "", r(result.yearlyRows.reduce((s, x) => s + x.yearContributions, 0)), r(result.totalInterestGross), r(result.totalTax), r(result.totalInterestGross - result.totalTax), r(result.finalBalance)]);
    const ws = XLSX.utils.aoa_to_sheet([[L.titleYearly], [], [L.rate, rate + "%"], [L.term, years + "y"], [L.currency, currency], [L.interestPayment, freqLabel], [], hdrs, ...rows]);
    ws["!cols"] = [12,16,14,18,12,14,16].map(w => ({ wch: w }));
    XLSX.utils.book_append_sheet(wb, ws, L.sheetYearly); XLSX.writeFile(wb, `deposit_yearly_${years}y.xlsx`);
  } else {
    const hdrs = [L.year, L.month, `${L.topUp} (${sym})`, `${L.grossInterest} (${sym})`, `${L.tax} (${sym})`, `${L.netInterest} (${sym})`, `${L.balance} (${sym})`];
    const rows = result.allRows.map(row => [`${L.year} ${row.year}`, `${L.month} ${row.monthInYear}`, r(row.contribution), r(row.grossInterest), r(row.tax), r(row.netInterest), r(row.balance)]);
    const ws = XLSX.utils.aoa_to_sheet([[L.titleMonthly], [], [L.rate, rate + "%"], [L.term, years + "y"], [L.currency, currency], [L.interestPayment, freqLabel], [], hdrs, ...rows]);
    ws["!cols"] = [10,10,14,16,10,14,16].map(w => ({ wch: w }));
    XLSX.utils.book_append_sheet(wb, ws, L.sheetMonthly); XLSX.writeFile(wb, `deposit_monthly_${years}y.xlsx`);
  }
}

